package org.proxima.survey.frontutils;

import java.io.Serializable;

public class Evaluation implements Serializable{
	
	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;
	private Long userID;
	private static int totalQuestions;
	private int points;
	
	
	
	public Evaluation() {
		super();
	}

	public Evaluation(Long userID, int points) {
		super();
		this.userID = userID;
		this.points = points;
	}

	public Long getUserID() {
		return userID;
	}

	public void setUserID(Long userID) {
		this.userID = userID;
	}

	public static int getTotalQuestions() {
		return totalQuestions;
	}

	public static void setTotalQuestions(int totalQuestions) {
		Evaluation.totalQuestions = totalQuestions;
	}

	public int getPoints() {
		return points;
	}

	public void setPoints(int points) {
		this.points = points;
	}
	
	
	
}
